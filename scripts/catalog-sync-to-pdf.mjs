if (apply) {
  const stamp = new Date().toISOString().replace(/[:.]/g, "-");
  fs.writeFileSync(`catalog-sync-backup-${stamp}.json`, JSON.stringify(s, null, 2));
  const removeSet = new Set(remove.map((p) => p.ean));
  s.products = s.products.filter((p) => !removeSet.has(p.ean));
  s.stock = s.stock.filter((r) => !removeSet.has(r.ean));
  for (const combo of s.combos || []) {
    combo.components = (combo.components || []).filter((k) => !removeSet.has(k.ean));
  }
  await col.replaceOne({ _id: "store" }, { data: s });
  console.log(`\nAPPLIED. Backup: catalog-sync-backup-${stamp}.json | catalog now: ${s.products.length}`);
} else {
  console.log("\n(dry-run only — re-run with --apply to delete)");
}
