const apply = process.argv.includes("--apply");
const keepSet = new Set(KEEP.map((x) => x.trim()));
const uri = (fs.readFileSync(".env.local", "utf8").match(/^MONGODB_URI=(.*)$/m) || [])[1].trim();

const c = new MongoClient(uri, { serverSelectionTimeoutMS: 15000 });
await c.connect();
const col = c.db("inventory").collection("app");
const doc = await col.findOne({ _id: "store" });
const s = doc.data;

const keep = s.products.filter((p) => keepSet.has(p.ean));
const remove = s.products.filter((p) => !keepSet.has(p.ean));

console.log("PDF unique EANs:", keepSet.size);
