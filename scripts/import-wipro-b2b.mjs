function buildText() {
  return ROWS.map(([asin, size, sku]) => [asin, size, sku].join("\t")).join("\n");
}

async function main() {
  const skus = new Set(ROWS.map((r) => r[2]));
  console.log(`Rows: ${ROWS.length} | distinct SKU (products): ${skus.size}`);

  const login = await fetch(`${BASE}/api/auth/login`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ role: "admin", password: ADMIN_PASSWORD }),
  });
  if (!login.ok) {
    console.error("Login failed:", login.status, await login.text());
    process.exit(1);
  }
  const authCookie = login.headers.get("set-cookie").split(";")[0];
  const cookie = `${authCookie}; channel=b2b`;
  const text = buildText();

  const preview = await fetch(`${BASE}/api/admin/import`, {
    method: "POST",
    headers: { "Content-Type": "application/json", cookie },
    body: JSON.stringify({ text, format: "asin", brand: BRAND, preview: true }),
  }).then((r) => r.json());
  console.log("PREVIEW:", JSON.stringify(preview.summary));

  const imported = await fetch(`${BASE}/api/admin/import`, {
    method: "POST",
    headers: { "Content-Type": "application/json", cookie },
    body: JSON.stringify({ text, format: "asin", brand: BRAND, preview: false }),
  }).then((r) => r.json());
  console.log("IMPORT RESULT:", JSON.stringify(imported.result));
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
